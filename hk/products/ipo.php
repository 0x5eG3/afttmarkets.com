新股價一般來說比其他股票起伏較大，風險也會較多。在買新股前，投資者應該注意以下5大因素：
<br>上市公司估值：公司估值又被稱為企業估值，指公司內在價值，包括公司的資產和獲利能力。一般來說，估值是透過過往的業績來衡量的。在認購IPO時，投資者會比較難得到關於公司財務的所有資訊。在這情況下，投資者可以透過公司前景和IPO熱度判斷該公司將來發展的潛能。
<br>上市公司股東陣容：投資者也應該留意上市公司股東的陣容，進一步瞭解企業管理方式、基礎及策略。如果某公司的股東為知名機構，這也可以提高新股的熱度和大眾的信心。
<br>上市公司業務：另外要留意的是上市公司的業務是否處於高增長期。即使公司管理做得更好，如果整體業務走勢差，估值增長的潛質也有機會被限制。
<br>上市公司業績及市場競爭力：投資者也應該考慮上市公司的業績和市場競爭力去衡量公司是否能持續盈利。雖然買入新股的投資者不能掌握到上市公司的真正財務狀況，但是他們可以衡量該公司此刻的表現和同業作比較。
<br>市場傾向及走勢：最後，投資者應該留意市場整體的傾向和走勢。在經濟衰退的時候，儘管公司業績出色，也難增長收入及盈利。</p>
							
