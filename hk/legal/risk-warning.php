<strong>1. 一般風險</strong>
<ol style="list-style-type:lower-alpha;">
    <li style="margin-bottom:0.3em"><em>低流動性風險</em>。流動性指市場參與者買入與賣出證券的能力。一般來說，市場上的訂單越多，流動性就越高。流動性非常重要，因為流動性較高時，投資者比較容易買賣證券，因而有可能支付或獲得更具競爭力的證券成交價。延長時段期間交易的流動性可能低於常規交易時段。因此，您在延長交易時段中提交的訂單可能只會被部分執行，甚至完全不被執行，也可能會獲得較不利的成交價。</li>
