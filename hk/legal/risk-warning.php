<h3 class="heading--section">外匯觸碰期權（適用於專業投資者）</h3>
<p class="text--body">外匯觸碰期權是二元期權的一種，其支付只有兩種可能的結果：可能是在到期日或之前發生事件時按照預定的固定金額向您支付投資收益，也可能是您損失期權中的投資金額。簡單地說，觸碰期權通常以「全有或全無」的支出結構持有至到期，並以簡單的「是」或「否」命題為依據：貨幣對*是否會在特定日期或之前觸碰設定價格？這與傳統的外匯期權不同，後者授予在特定日期或之前以設定價格買入或賣出貨幣對的權利（但無義務）。</p>
<p class="text--body">當您交易觸碰期權時，最大虧蝕額是已知的，根據是買入還是賣出倉位，在觸碰期權交易時，您的帳戶將全額保留權利金或名義/支付額（以代表最大虧蝕額的為准），也就是說，該產品不能用保證金交易。</p>
<p class="text--body">觸碰期權是否受監管取決於其目標資產。如果二元期權的目標資產是場外交易（譬如外匯即期交易），必須注意的是，場外交易市場在很大程度上是不受監管的，因為它的結構是分散的，交易直接在兩個交易對手之間進行，流動性分散在不同的場所。因此，與正規交易所的監管監督水準與透明度相比，場外交易市場的監管監督水準與透明度非常低，而正規交易所則需要維護公平、有序與透明的市場。 </p>
<p class="text--body">觸碰期權的可交易期限從 1 天到 12 個月不等。期限較短的觸碰期權風險較高，因為投資者可能會受到短期波動的影響，此外，在短時間內持續預測目標資產的表現同樣具有挑戰性。 </p>
<p class="text--body">除非您完全瞭解觸碰期權交易的基本方面及其相應風險，否則不應參與此類交易。通過交易不受監管的金融產品，投資者將得不到監管框架提供的保護，而這種保護只有在受監管的產品中才有。 </p>
<p class="text--body">*可用貨幣對包括：歐元兌美元、英鎊兌美元、歐元兌英鎊、美元兌日元、歐元兌日元與澳元兌美元。</p>
<p class="text--body">例如：</p>
<ul>
   <li style="margin-bottom:0.3em">如果您購買的是觸碰期權或非觸碰期權，則您可能虧蝕的最大金額就是預付的期權金（如果期權「觸碰」，您將獲得名義/支出金額）。您必須有能力預付這筆期權金，期權金會在交易時從您的帳戶中扣除，而且從保證金的角度來看，觸碰期權倉位的市場價值不能用作支持任何其他現有或全新的未平倉合約的杠杆。</li>
   <li style="margin-bottom:0.3em">如果您賣出的是非觸碰期權或觸碰期權，則您會預收期權金（您保留全部期權金，但是如果期權「觸碰」，您可能需要支付名義/支出金額）。期權金在交易時記入帳戶，但是全部的名義/支出金額將從帳戶中保留，從保證金的角度來看，在觸碰期權的有效期內不能用於支持任何其他現有或新的未平倉合約。</li>
</ul>

<h3 class="heading--section">差價合約（適用於專業投資者）</h3>
<p class="text--body">CFD或差價合約（Contract for Difference）是雙方就金融工具或證券的買賣差價進行交換的協議。該產品允許您對特定資產（如股票）的未來價值漲跌進行預測。如果您的觀點被證明是正確的，則您將會從價值差額（減去成本）中獲利。如果您的觀點被證明是錯誤的，您必須支付相應的價值差額（外加費用）。由於與目標資產掛鉤，差價合約的價值取決於該資產。差價合約始終採用保證金交易（參見上段關於外匯交易的內容）。差價合約通常以盛寶為交易對手進行交易，但某些差價合約可能在受監管市場上交易。不過，價格始終隨著目標產品的價格變動，而目標產品在大多數情況下是在受監管的市場上交易的。個股差價合約的價格與流動性反映了該股票在允許交易市場上的價格與流動性，而指數差價合約則是場外交易（OTC）產品，其價格由盛寶根據目標股票的價格與流動性、期貨市場、未來股息估計值、利率影響等因素確定。</p> 
<p class="text--body">請注意，由於差價合約是保證金交易，因此根據您在盛寶的資金情況，您可以持有更大的倉位。正因如此，目標金融工具相對較小的負面或正面波動都可能對您的投資產生重大影響，從而導致差價合約交易的風險相對較高。這樣，即使保證金存款相對較少，潛在收益也會非常高。如果您的保證金交易總風險超過您的保證金存款，您的虧蝕就有可能超過您的保證金存款。</p>
<p class="text--body">差價合約投資通常被認為是一種有風險的投資形式。如果您過去一直採取保守的投資方式，則在考慮投資任何集體投資計畫之前，您可能希望進一步諮詢專業人士的意見。您必須認識到，您可能會虧蝕全部投資資金。</p>

