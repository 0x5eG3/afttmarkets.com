<p style="margin-left: 1.0rem;">對於可在延長交易時段期間進行交易的金融產品，在此期間所收到的價格更新會影響帳戶中的初始保證金，但不會影響當中的維持保證金。不過，由於延長交易時段的交易活動（包括其他金融產品的交易或貨幣波動）所致，您的保證金佔用比率仍可能在延長時段交易期間產生變化。若您在延長交易時段期間達到或違反保證金要求，盛寶在常規交易時段之前可能不會將此等金融產品的任何和所有合約與保證金持倉平倉，但或會立即在常規交易時段將任何其他金融產品平倉。</p>
<strong>4.	賬戶屏障</strong>
<p style="margin-left: 1.0rem;">對於可在延長交易時段期間進行交易的金融產品，您的帳戶屏障不會因在延長時段交易期間收到的價格更新而受到觸發，而只會因此等金融產品在常規交易時段期間的價格更新而受到觸發。</p>
<p class="text--body">參與延長時段交易，即表示您明確確認並同意在延長交易時段投資所特有的風險及規則。盛寶可能無法預測及描述延長時段交易中可能出現的所有特殊交易風險。因此您同意，對於您因參與延長時段交易而承擔的任何風險（不論上文提及與否），盛寶概不負責。</p>
<p class="text--body">如本風險警告與盛寶一般交易條款有任何不一致之處，請以一般交易條款為准。</p>
