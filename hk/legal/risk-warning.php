    <li style="margin-bottom:0.3em"><em>價格變動風險/em&gt;。延長交易時段期間的證券成交價可能無法反映常規時段交易期間的價格。因此，與常規交易時段相比，您在延長交易時段時所獲得的成交價可能較為不利。此外，指數或投資組合的證券將不會像在常規交易時段期間一樣定期交易，甚至可能完全無法交易。這或會導致延長交易時段期間的價格無法反映該等證券之後的開市價。</em></li>
    <li style="margin-bottom:0.3em"><em>市場斷連風險</em>。視乎延長交易時段系統或當日時間而定，某個延長交易時段系統上顯示的價格可能無法反映其他同時段運作的延長交易時段系統上相同證券的成交價。因此，與您在其他延長交易時段系統中獲得的價格相比，您在某個延長交易時段系統中獲得的價格可能較為不利。</li>
    <li style="margin-bottom:0.3em"><em>消息發佈風險</em>。一般情況下，發行人會在常規交易時段後發佈可能影響其證券價格的新聞公告。同樣，重要的財務資訊也經常在常規交易時段之外公佈。在延長交易時段中，這些公告可能會在交易期間發佈，如果再加上較低的流動性和較高波動率風險，可能會對證券價格造成誇大和不可持續的影響。</li>
    <li style="margin-bottom:0.3em" "=""><em>價差擴大風險</em>。 價差指證券買入價與賣出價之間的差額。延長交易時段的流動性較低，波動率較高，可能導致特定證券的價差高於正常水準。</li>
